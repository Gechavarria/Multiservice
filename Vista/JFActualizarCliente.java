    private void tTablaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tTablaClienteMouseClicked
           int select = tTablaCliente.getSelectedRow();
        if (select > 0) {
            tID.setText(tTablaCliente.getValueAt(select, 0).toString());
            tRazonSocial.setText(tTablaCliente.getValueAt(select, 1).toString());
            tRuc.setText(tTablaCliente.getValueAt(select, 2).toString());
            tNombreCliente.setText(tTablaCliente.getValueAt(select, 3).toString());
            tApellidoCliente.setText(tTablaCliente.getValueAt(select, 4).toString());
            tDireccion.setText(tTablaCliente.getValueAt(select, 5).toString());
            tCelular.setText(tTablaCliente.getValueAt(select, 6).toString());
            tCorreo.setText(tTablaCliente.getValueAt(select, 7).toString());
            
        } else {
            IO.imprimir("Fila no seleccionada");
        }   
       
        
    }//GEN-LAST:event_tTablaClienteMouseClicked

