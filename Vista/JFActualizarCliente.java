    private void tBuscarClienteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tBuscarClienteKeyTyped
           tBuscarCliente.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent e) {
           
                filter.setRowFilter(RowFilter.regexFilter("(?i)" + tBuscarCliente.getText(), 3));
                filter.setRowFilter(RowFilter.regexFilter("(?i)" + tBuscarCliente.getText(), 4));
            }

        });
        filter = new TableRowSorter(tTablaCliente.getModel());
        tTablaCliente.setRowSorter(filter); 
    }//GEN-LAST:event_tBuscarClienteKeyTyped

